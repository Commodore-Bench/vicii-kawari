char dst[3][16];
char* bin(int n, int v) {
   int bit=128;
   for(int b=0;b<4;b++) {
	   if (v & bit) 
		   dst[n][b] = '1';
           else  
		   dst[n][b] = '0';
	   bit=bit/2;
   }
   return dst[n];
}

